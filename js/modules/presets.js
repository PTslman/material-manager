    // Render presets
    renderPresets: function(section) {
        const container = document.getElementById('presetList');
        if (!container) return;
        
        const items = this.lists[section] || [];
        
        if (items.length === 0) {
            container.innerHTML = '<p style="text-align:center;color:var(--text-muted);padding:1rem;">لا توجد مواد في هذه القائمة</p>';
            return;
        }
        
        container.innerHTML = items.map(function(item) {
            return '<div class="preset-item" onclick="Presets.addPreset(\'' + item.replace(/'/g, "\\'") + '\', \'' + section + '\')">' +
                '<i class="fas fa-plus-circle"></i>' +
                '<span>' + item + '</span>' +
                '</div>';
        }).join('');
        
        // Update tabs
        document.querySelectorAll('.preset-tab').forEach(function(tab) {
            tab.classList.toggle('active', tab.dataset.section === section);
        });
    },
    
    // Add preset item
    addPreset: function(name, section) {
        // Check if material already exists
        const exists = Materials.allMaterials.some(function(m) {
            return m.name === name && m.section === section;
        });
        
        if (exists) {
            UI.showNotification('المادة موجودة بالفعل في هذا القسم', 'error');
            return;
        }
        
        Materials.addMaterial(name, 1, 'كغ', section);
    }
